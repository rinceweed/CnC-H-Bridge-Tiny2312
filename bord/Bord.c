/*!

  Init die AVR bordjie

*/
/*-------------------------------------------------------------------------------------------------------------------------------*/
void Bord_Init(void)
{
  HBRIDGE_DDR  = 0xff;
  HBRIDGE_PORT = 0x00;//Hbridge control moet laag wees met begin

  CONTROL_DDR  = (CONTROL_OUT_MASK)&(~CONTROL_IN_MASK); // inputs want it low
  CONTROL_PORT = (~CONTROL_OUT_MASK)&(CONTROL_IN_MASK);

  return;
}
